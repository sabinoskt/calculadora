from util.io import get_int_number, get_float_number

def adicao(num1: int, num2: int) -> int:
    return num1 + num2

def subtracao(num1: int, num2: int) -> int:
    return num1 - num2

def multiplicacao(num1: int, num2: int) -> int:
    return num1 * num2

def divisao(num1: float, num2: float) -> float:
    return num1 / num2
